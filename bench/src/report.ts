import { writeFile } from "node:fs/promises";
import { join } from "node:path";
import { ensureManifest } from "./manifest.js";
import { OVERRIDES_PATH, SCORES_PATH } from "./score.js";
import { buildReportHtml } from "./html.js";
import {
  OverridesSchema,
  ScoresSchema,
  type ModelMetrics,
  type Overrides,
  type Scores,
} from "./types.js";
import { RESULTS_DIR, readJsonIfExists } from "./util.js";

const RESULTS_MD_PATH = join(RESULTS_DIR, "RESULTS.md");
const REPORT_HTML_PATH = join(RESULTS_DIR, "report.html");

function pct(value: number | null): string {
  return value === null ? "–" : `${(100 * value).toFixed(0)}%`;
}

function num(value: number | null, digits: number, suffix = ""): string {
  return value === null ? "–" : `${value.toFixed(digits)}${suffix}`;
}

function money(value: number | null): string {
  return value === null ? "–" : `$${value.toFixed(4)}`;
}

function leaderboardRow(m: ModelMetrics): string {
  return `| ${m.model} | ${m.provider} | ${pct(m.meanRecall)} | ${pct(m.anyRecall)} | ${pct(m.flakiness)} | ${num(m.extrasPerRun, 1)} | ${pct(m.noBugsCleanRate)} | ${num(m.latencyMedianSeconds, 1, "s")} / ${num(m.latencyP95Seconds, 1, "s")} | ${money(m.meanCostPerRun)} | ${money(m.totalCost)} | ${m.failedRuns || ""} |`;
}

export function buildResultsMarkdown(scores: Scores): string {
  const lines = [
    "# Visual reasoning benchmark results",
    "",
    "Generated by `pnpm bench:report`. Do not edit by hand — re-run the pipeline instead.",
    "",
    `- **Generated:** ${scores.generatedAt}`,
    `- **Prompt:** \`${scores.prompt}\` (sha256 \`${scores.promptHash}\`)`,
    `- **Reasoning effort:** \`${scores.reasoningEffort}\` (fixed for all models; per-provider mapping documented in the library)`,
    `- **Repeats:** ${scores.repeats} per model × image`,
    `- **Judge:** \`${scores.judgeModel}\` (text-only, prompt ${scores.judgePromptVersion}), ${scores.overrideCount} manually overridden cell(s)`,
    "",
    "Recall = mean per-expected-issue detection rate across reps. Flakiness = share of expected",
    "issues detected in some but not all reps. Extras/run = mean reported issues the judge could",
    "not match to an expected issue. No composite score: with a small issue set, rank by the",
    "columns that matter to you (default sort: recall desc, extras asc).",
    "",
    "| Model | Provider | Recall | Recall (any rep) | Flakiness | Extras/run | No-bugs clean | Latency med/p95 | Cost/run | Total cost | Failed |",
    "| --- | --- | --- | --- | --- | --- | --- | --- | --- | --- | --- |",
    ...scores.models.map(leaderboardRow),
    "",
    "See `report.html` (open from the repo) for per-image drill-down and judge-verdict overrides.",
    "",
  ];
  return lines.join("\n");
}

async function main(): Promise<void> {
  const manifest = await ensureManifest();
  const scoresRaw = await readJsonIfExists(SCORES_PATH);
  if (scoresRaw === undefined) {
    throw new Error(`${SCORES_PATH} not found. Run "pnpm bench:score" first.`);
  }
  const scores = ScoresSchema.parse(scoresRaw);
  const overridesRaw = await readJsonIfExists(OVERRIDES_PATH);
  const overrides: Overrides =
    overridesRaw === undefined ? {} : OverridesSchema.parse(overridesRaw);

  await writeFile(RESULTS_MD_PATH, buildResultsMarkdown(scores), "utf8");
  await writeFile(REPORT_HTML_PATH, buildReportHtml(scores, manifest, overrides), "utf8");
  console.log(`Wrote ${RESULTS_MD_PATH}`);
  console.log(`Wrote ${REPORT_HTML_PATH}`);
}

const isDirectRun = process.argv[1]?.endsWith("report.ts") ?? false;
if (isDirectRun) {
  main().catch((error: unknown) => {
    console.error(error instanceof Error ? error.message : error);
    process.exitCode = 1;
  });
}
